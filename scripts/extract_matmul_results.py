#!/usr/bin/env python3
"""Extract matmul functional results from Mode 2 CSV files.

Reads rtl_results_ch{N}.csv generated by AiM-Cosim Mode 2 and extracts
RDAF16 (or RDMAC16) results into a clean text format for cross-platform
comparison (e.g., GPU verification).

Output format:
  - One row per matmul output row (r = 0..R-1)
  - Each row has 16 BF16 values (one per bank), in both hex and float
  - Header contains P, Q, R, channel, seed metadata

Usage:
  python extract_matmul_results.py --csv rtl_results_ch0.csv \
      --p 1 --q 256 --r 256 -o matmul_output.txt

  # Batch mode: extract from all channels in a directory
  python extract_matmul_results.py --csv-dir /path/to/csvs/ \
      --p 1 --q 256 --r 256 --channel 0 -o matmul_output.txt
"""

import argparse
import glob
import os
import struct
import sys


# GDDR6 command enum (matches verilator_bridge.h)
CMD_RDMAC16 = 19
CMD_RDAF16 = 20


def bf16_hex_to_float(hex_str: str) -> float:
    """Convert BF16 hex string to float32."""
    val = int(hex_str, 16)
    # BF16 is upper 16 bits of float32
    f32_bits = val << 16
    return struct.unpack('f', struct.pack('I', f32_bits))[0]


def parse_csv(csv_path: str, target_cmd: int = CMD_RDAF16):
    """Parse rtl_results CSV and extract target command results.

    Returns list of (bank_id, bf16_hex_list) grouped by command occurrence.
    Each RDAF16/RDMAC16 block = 16 rows (one per bank) = one matmul output row.
    """
    results = []  # List of dicts: {bank_id: bf16_hex}
    current_block = {}

    with open(csv_path, 'r') as f:
        header = f.readline().strip()
        assert 'cycle,command,bank_id' in header, f"Unexpected CSV header: {header}"

        prev_cycle = None
        for line in f:
            line = line.strip()
            if not line:
                continue
            parts = line.split(',')
            cycle = int(parts[0])
            command = int(parts[1])
            bank_id = int(parts[2])
            data_hex = parts[5].strip()

            if command != target_cmd:
                continue

            # New block when cycle changes (all 16 banks share same cycle)
            if prev_cycle is not None and cycle != prev_cycle:
                if current_block:
                    results.append(current_block)
                    current_block = {}

            # First element of data_hex is the result scalar
            first_hex = data_hex.split()[0]
            current_block[bank_id] = first_hex
            prev_cycle = cycle

        # Don't forget last block
        if current_block:
            results.append(current_block)

    return results


def write_output(results, p, q, r, channel, seed, output_path, cmd_name):
    """Write extracted results to text file."""
    expected_rows = p * r

    with open(output_path, 'w') as f:
        f.write(f"# AiM Matmul Functional Results\n")
        f.write(f"# C[{p}x{r}] = A[{p}x{q}] x B[{q}x{r}]\n")
        f.write(f"# P={p}, Q={q}, R={r}\n")
        f.write(f"# Channel: {channel}, init_seed: {seed}\n")
        f.write(f"# Source command: {cmd_name}\n")
        f.write(f"# Rows: {len(results)} (expected: {expected_rows})\n")
        f.write(f"#\n")
        f.write(f"# Format: row_idx  bank0_hex bank1_hex ... bank15_hex  |  bank0_float bank1_float ... bank15_float\n")
        f.write(f"#\n")

        for row_idx, block in enumerate(results):
            hex_vals = []
            float_vals = []
            for b in range(16):
                h = block.get(b, "0")
                hex_vals.append(f"{int(h, 16):04x}")
                float_vals.append(f"{bf16_hex_to_float(h):12.5e}")

            hex_str = " ".join(hex_vals)
            float_str = " ".join(float_vals)
            f.write(f"{row_idx:6d}  {hex_str}  |  {float_str}\n")

        # Summary
        f.write(f"#\n")
        f.write(f"# Total output rows: {len(results)}\n")
        f.write(f"# Total BF16 values: {len(results) * 16}\n")

    if len(results) != expected_rows:
        print(f"  WARNING: Expected {expected_rows} rows but got {len(results)}", file=sys.stderr)


def main():
    parser = argparse.ArgumentParser(description='Extract matmul results from Mode 2 CSV')
    parser.add_argument('--csv', type=str, help='Path to single CSV file')
    parser.add_argument('--csv-dir', type=str, help='Directory containing rtl_results_ch*.csv')
    parser.add_argument('--channel', type=int, default=0, help='Channel to extract (default: 0)')
    parser.add_argument('--p', type=int, default=1, help='Batch size')
    parser.add_argument('--q', type=int, required=True, help='Input dimension')
    parser.add_argument('--r', type=int, required=True, help='Output dimension')
    parser.add_argument('--seed', type=int, default=42, help='Init seed (for metadata)')
    parser.add_argument('--cmd', choices=['rdaf16', 'rdmac16'], default='rdaf16',
                        help='Which command results to extract (default: rdaf16)')
    parser.add_argument('-o', '--output', required=True, help='Output text file path')
    args = parser.parse_args()

    target_cmd = CMD_RDAF16 if args.cmd == 'rdaf16' else CMD_RDMAC16
    cmd_name = 'RDAF16' if args.cmd == 'rdaf16' else 'RDMAC16'

    # Find CSV file
    if args.csv:
        csv_path = args.csv
    elif args.csv_dir:
        csv_path = os.path.join(args.csv_dir, f"rtl_results_ch{args.channel}.csv")
    else:
        parser.error("Either --csv or --csv-dir must be specified")

    if not os.path.exists(csv_path):
        print(f"ERROR: CSV not found: {csv_path}", file=sys.stderr)
        sys.exit(1)

    print(f"Extracting {cmd_name} results from: {csv_path}")
    results = parse_csv(csv_path, target_cmd)

    if not results:
        # Fallback: try RDMAC16 if RDAF16 not found
        if target_cmd == CMD_RDAF16:
            print(f"  No {cmd_name} results found, trying RDMAC16...", file=sys.stderr)
            results = parse_csv(csv_path, CMD_RDMAC16)
            cmd_name = 'RDMAC16 (fallback)'

    if not results:
        print(f"ERROR: No results found in {csv_path}", file=sys.stderr)
        sys.exit(1)

    write_output(results, args.p, args.q, args.r, args.channel, args.seed,
                 args.output, cmd_name)
    print(f"  Saved: {args.output} ({len(results)} rows × 16 banks = {len(results)*16} BF16 values)")


if __name__ == '__main__':
    main()
